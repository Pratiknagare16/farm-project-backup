const fs = require('fs');
const path = require('path');

const i18nDir = path.join(__dirname, '../static/i18n');

// Helper to read JSON
const readJson = (filePath) => {
    try {
        if (fs.existsSync(filePath)) {
            const content = fs.readFileSync(filePath, 'utf8');
            return JSON.parse(content);
        }
    } catch (e) {
        console.error(`Error reading ${filePath}:`, e);
    }
    return {};
};

// Helper to write JSON
const writeJson = (filePath, data) => {
    try {
        fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
    } catch (e) {
        console.error(`Error writing ${filePath}:`, e);
    }
};

// Function to patch a specific language file
const patchLanguage = (langCode, updates) => {
    const filePath = path.join(i18nDir, `${langCode}.json`);
    console.log(`Patching ${langCode}.json...`);
    
    const data = readJson(filePath);
    
    // Deep merge updates
    for (const section in updates) {
        if (!data[section]) data[section] = {};
        for (const key in updates[section]) {
            data[section][key] = updates[section][key];
        }
    }
    
    writeJson(filePath, data);
    console.log(`Updated ${langCode}.json successfully.`);
};

